/**
 * normalizeKeyword - 키워드 정규화 (한자→한글, 약어→전체명)
 * 
 * 같은 의미지만 다르게 표현된 키워드를 통일합니다.
 * 예: 尹 → 윤석열, 與 → 여당, 野 → 야당
 */
function normalizeKeyword(word: string): string {
    const normalizationMap: Record<string, string> = {
        // 정치인 (한자→한글)
        '尹': '윤석열',
        '李': '이재명',
        // 정당 (한자→한글)
        '與': '여당',
        '野': '야당',
        // 기관 (약어→전체)
        'TK': '대구경북',
        'PK': '부산경남',
        // 연예인 (약어→전체)
        '민희진': '민희진',
        '뉴진스': '뉴진스',
        'NewJeans': '뉴진스',
        // 기타
        '內': '내',
        '外': '외',
    }
    
    return normalizationMap[word] || word
}

/**
 * tokenize - 제목을 키워드 배열로 분리
 *
 * 특수문자 제거 후 공백 기준 분리, 2글자 미만 제거.
 * 키워드 정규화를 통해 한자/약어를 한글로 통일.
 * 
 * 예시:
 * tokenize("尹 대통령 긴급 기자회견") → ["윤석열", "긴급", "기자회견"]
 */
export function tokenize(text: string): string[] {
    const words = text
        .replace(/[^\wㄱ-ㅎㅏ-ㅣ가-힣\s]/g, ' ')
        .split(/\s+/)
        .map((w) => {
            let normalized = w
            
            // 1. "XXX와", "XXX과" 형태의 조사 제거
            if (normalized.endsWith('와') || normalized.endsWith('과')) {
                normalized = normalized.slice(0, -1)
            }
            
            // 2. "한국XXX" → "XXX" 정규화 (기관명, 4글자 이상만)
            if (normalized.startsWith('한국') && normalized.length > 4) {
                normalized = normalized.slice(2)
            }
            
            return normalized
        })
        .filter((w) => (w.length >= 2 || ALLOWED_ONE_CHAR_KEYWORDS.has(w)) && !GROUPING_STOPWORDS.has(w))
        .map(normalizeKeyword)
    return Array.from(new Set(words))
}
